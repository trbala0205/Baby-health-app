package com.main.java.babyapp.model;

public class ChildDetailsDAO {

	private int child_id;
	private String baby_image;
	private String baby_name;
	private String baby_dob;
	
	public int getChild_id() {
		return child_id;
	}
	public void setChild_id(int child_id) {
		this.child_id = child_id;
	}
	public String getBaby_image() {
		return baby_image;
	}
	public void setBaby_image(String baby_image) {
		this.baby_image = baby_image;
	}
	public String getBaby_name() {
		return baby_name;
	}
	public void setBaby_name(String baby_name) {
		this.baby_name = baby_name;
	}
	public String getBaby_dob() {
		return baby_dob;
	}
	public void setBaby_dob(String baby_dob) {
		this.baby_dob = baby_dob;
	}
	
}
